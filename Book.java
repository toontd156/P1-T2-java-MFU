import java.util.*;

public class Book {
    private String bName;
    private double bPrice;

    public void setBookName(String BName) {
        bName = BName;
    }

    public String getBookName() {
        return bName;
    }

    public void setBookPrice(double BPrice) {
        bPrice = BPrice;
    }

    public double getBookPrice() {
        return bPrice;
    }
}
